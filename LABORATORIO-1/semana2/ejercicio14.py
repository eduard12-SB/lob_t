"""
Mayúsculas y minúsculas: Escribe un programa que convierta una cadena dada por el usuario a
mayúsculas y luego a minúsculas, imprimiendo cada resultado.
"""

cadena = input("Introduce una cadena: ")
cadena_mayusculas = cadena.upper()
cadena_minusculas = cadena.lower()
print("Mayuscula:", cadena_mayusculas)
print("Minúsculas:", cadena_minusculas)